public class Char {
    public static void main(String[] args){
        char symbol1 = 1099; //Индекс символа в таблице UTF-8
        char symbol2 = 'ы'; //Прямое присваивание переменной
        char symbol3 = '\u044B'; //Через шестнадцатеричную форму Unicode
//Печатаем результат переменных на экран
        System.out.println(symbol1);
        System.out.println(symbol2);
        System.out.println(symbol3);

    }}
